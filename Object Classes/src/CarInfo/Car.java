package CarInfo;

public class Car {
    public String brand;
    public String model;
    public int horsrePower;

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public void setHorsrePower(int horsrePower) {
        this.horsrePower = horsrePower;
    }

    public String getModel() {
        return model;
    }

    public String getBrand() {
        return brand;
    }

    public int getHorsrePower() {
        return horsrePower;
    }


}
